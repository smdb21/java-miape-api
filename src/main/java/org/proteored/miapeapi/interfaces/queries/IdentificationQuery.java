package org.proteored.miapeapi.interfaces.queries;

import java.util.List;

import org.proteored.miapeapi.exceptions.MiapeDatabaseException;
import org.proteored.miapeapi.exceptions.MiapeSecurityException;
import org.proteored.miapeapi.interfaces.msi.IdentifiedPeptide;
import org.proteored.miapeapi.xml.pride.autogenerated.ExperimentType.MzData.SpectrumList.Spectrum;

public interface IdentificationQuery {
	// TODO MOVE THIS CLASS TO THE MIAPE-API MODULE
	/**
	 * Gets the information of the peptides that explains certain protein given
	 * as an accession
	 * 
	 * @param proteinACC
	 * @param matchModifier
	 *            : indicating how the protein accession is searched
	 *            <ul>
	 *            <li>0 exact string</li>
	 *            <li>1 containing the string</li>
	 *            </ul>
	 * @param userName
	 * @param pass
	 * @return
	 * @throws MiapeSecurityException
	 * @throws MiapeDatabaseException
	 */
	public List<IdentifiedPeptide> getPeptidesFromProteinACC(String proteinACC, int matchModifier,
			String userName, String pass) throws MiapeDatabaseException, MiapeSecurityException;

	/**
	 * Gets the informatino of the peptides that have a certain sequence
	 * 
	 * @param sequence
	 * @param userName
	 * @param pass
	 * @return
	 * @throws MiapeSecurityException
	 * @throws MiapeDatabaseException
	 */
	public List<IdentifiedPeptide> getPeptidesFromSequence(String sequence, String userName,
			String pass) throws MiapeDatabaseException, MiapeSecurityException;

	/**
	 * Gets the spectrum information from a peptide id
	 * 
	 * @param peptideID
	 * @param userName
	 * @param pass
	 * @return
	 * @throws MiapeSecurityException
	 * @throws MiapeDatabaseException
	 */
	public Spectrum getSpectrumFromPeptideID(int peptideID, String userName, String pass)
			throws MiapeDatabaseException, MiapeSecurityException;
}
