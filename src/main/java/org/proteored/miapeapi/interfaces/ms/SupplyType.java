package org.proteored.miapeapi.interfaces.ms;

import java.util.Map;

import org.proteored.miapeapi.xml.pride.autogenerated.CvParamType;

/**
 * The Enum SupplyType. Whether the sprayer is fed (by, for example,
 * chromatography or CE) or is loaded with sample once (before spraying).
 */
public enum SupplyType {

	STATIC("static"), FED("fed"), UKNOWN("unknown");

	private final String description;
	public final static String STATIC_FIELD = "MS:1000320";

	private SupplyType(String description) {
		this.description = description;
	}

	public String getDescription() {
		return description;
	}

	public static SupplyType getType(String description) {
		for (SupplyType type : SupplyType.values()) {
			if (type.description.equalsIgnoreCase(description)) {
				return type;
			}
		}
		return UKNOWN;
	}

	public static SupplyType getSupplyType(Map<String, CvParamType> cvParams) {
		for (String accession : cvParams.keySet()) {
			if (STATIC_FIELD.equals(accession)) {
				return STATIC;
			}
		}
		return null;
	}
}
