import org.proteored.miapeapi.xml.pride.autogenerated.InstrumentDescriptionType;
import org.proteored.miapeapi.xml.pride.util.Utils;

public class MaldiImpl implements Maldi {
	private final InstrumentDescriptionType instrument;
	private final Map<String, CvParamType> cvSourceParams;
	private final ControlVocabularyManager cvManager;

	public MaldiImpl(InstrumentDescriptionType instrument, Map<String, CvParamType> cvSourceParams,
			ControlVocabularyManager cvManager) {
		this.instrument = instrument;
		this.cvSourceParams = cvSourceParams;
		this.cvManager = cvManager;
	}

	@Override
	public String getExtraction() {
		return IonOpticsType.getExtraction(cvSourceParams);
	}

	@Override
	public String getLaser() {
		return LaserType.getLaser(cvSourceParams, cvManager);

	}

	@Override
	public String getMatrix() {
		// TODO Auto-generated method stub
		return null;
	}

	@Override
	public String getName() {
		if (instrument != null && instrument.getSource() != null) {
			String sourceName = Utils.getSourceName(instrument.getSource(), null, cvManager);
			if (sourceName != null)
				return sourceName;
		}
		return "Maldi source";
	}

	@Override
	public String getLaserParameters() {
		// TODO Auto-generated method stub
		return null;
	}

	@Override
	public String getPlateType() {
		// TODO Auto-generated method stub
		return null;
	}

	@Override
	public String getDissociation() {
		// TODO Auto-generated method stub
		return null;
	}

	@Override
	public String getLaserWaveLength() {
		// TODO Auto-generated method stub
		return null;
	}

	@Override
	public String getDissociationSummary() {
		// TODO Auto-generated method stub
		return null;
	}

}
