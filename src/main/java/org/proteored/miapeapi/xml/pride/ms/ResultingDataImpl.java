package org.proteored.miapeapi.xml.pride.ms;

import org.proteored.miapeapi.interfaces.ms.ResultingData;
import org.proteored.miapeapi.xml.pride.autogenerated.SourceFileType;

public class ResultingDataImpl implements ResultingData {
	private final SourceFileType sourceFile;

	public ResultingDataImpl(SourceFileType sourceFile) {
		this.sourceFile = sourceFile;
	}

	@Override
	public String getName() {
		if (sourceFile != null) {
			return sourceFile.getNameOfFile();
		}
		return null;
	}

	@Override
	public String getDataFileUri() {
		if (sourceFile != null) {
			return sourceFile.getPathToFile();
		}
		return null;
	}

	@Override
	public String getDataFileType() {
		if (sourceFile != null)
			return sourceFile.getFileType();
		return null;
	}

	@Override
	public String getAdditionalUri() {
		// TODO Auto-generated method stub
		return null;
	}

	@Override
	public String getSRMUri() {
		// TODO Auto-generated method stub
		return null;
	}

	@Override
	public String getSRMType() {
		// TODO Auto-generated method stub
		return null;
	}

	@Override
	public String getSRMDescriptor() {
		// TODO Auto-generated method stub
		return null;
	}

}
