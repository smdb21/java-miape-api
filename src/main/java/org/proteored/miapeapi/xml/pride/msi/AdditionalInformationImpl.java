package org.proteored.miapeapi.xml.pride.msi;

import org.proteored.miapeapi.interfaces.msi.MSIAdditionalInformation;
import org.proteored.miapeapi.xml.pride.autogenerated.CvParamType;
import org.proteored.miapeapi.xml.pride.autogenerated.UserParamType;

public class AdditionalInformationImpl implements MSIAdditionalInformation {
	private String name = "";
	private String value = "";

	public AdditionalInformationImpl(String name, String value) {
		this.name = name;
		this.value = value;
	}

	public AdditionalInformationImpl(CvParamType cvParam) {
		if (cvParam != null) {
			this.name = cvParam.getName();
			this.value = cvParam.getValue();
		}
	}

	public AdditionalInformationImpl(UserParamType userParam) {
		if (userParam != null) {
			this.name = userParam.getName();
			this.value = userParam.getValue();
		}
	}

	@Override
	public String getName() {
		return name;
	}

	@Override
	public String getValue() {
		return value;
	}

}
