package org.proteored.miapeapi.xml.ms;

import org.proteored.miapeapi.interfaces.ms.Spectrometer;
import org.proteored.miapeapi.xml.ms.autogenerated.MSSpectrometer;

public class SpectrometerImpl extends MiapeEquipmentImpl implements Spectrometer {
	private final MSSpectrometer xmlSpectrometer;

	public SpectrometerImpl(MSSpectrometer spectrometer) {
		super(spectrometer);
		this.xmlSpectrometer = spectrometer;

	}

	@Override
	public String getCustomizations() {
		return this.xmlSpectrometer.getCustomizations();
	}
}
