package org.proteored.miapeapi.xml.ms.adapter;

import org.proteored.miapeapi.cv.ms.InstrumentModel;
import org.proteored.miapeapi.cv.ms.InstrumentVendor;
import org.proteored.miapeapi.cv.ms.SpectrometerName;
import org.proteored.miapeapi.interfaces.Adapter;
import org.proteored.miapeapi.interfaces.Equipment;
import org.proteored.miapeapi.xml.ms.autogenerated.MIAPEEquipmentType;
import org.proteored.miapeapi.xml.ms.autogenerated.ObjectFactory;
import org.proteored.miapeapi.xml.ms.util.MsControlVocabularyXmlFactory;

public class EquipmentAdapter implements Adapter<MIAPEEquipmentType> {
	private final Equipment equipment;
	private final ObjectFactory factory;
	private final MsControlVocabularyXmlFactory cvFactory;

	public EquipmentAdapter(Equipment equipment, ObjectFactory factory,
			MsControlVocabularyXmlFactory cvFactory) {
		this.equipment = equipment;
		this.factory = factory;
		this.cvFactory = cvFactory;
	}

	@Override
	public MIAPEEquipmentType adapt() {
		MIAPEEquipmentType xmlEquipment = factory.createMIAPEEquipmentType();
		xmlEquipment.setCatalogNumber(equipment.getCatalogNumber());
		xmlEquipment.setComments(equipment.getComments());
		xmlEquipment.setDescription(equipment.getDescription());
		xmlEquipment.setManufacturer(cvFactory.createCV(equipment.getManufacturer(), null,
				InstrumentVendor.getInstance(cvFactory.getCvManager())));
		xmlEquipment.setModel(cvFactory.createCV(equipment.getModel(), null,
				InstrumentModel.getInstance(cvFactory.getCvManager())));
		xmlEquipment.setName(cvFactory.createCV(equipment.getName(), null,
				SpectrometerName.getInstance(cvFactory.getCvManager())));
		xmlEquipment.setParameters(equipment.getParameters());
		xmlEquipment.setURI(equipment.getUri());
		xmlEquipment.setVersion(equipment.getVersion());
		return xmlEquipment;
	}

}
