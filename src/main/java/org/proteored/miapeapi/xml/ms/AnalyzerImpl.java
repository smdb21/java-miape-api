package org.proteored.miapeapi.xml.ms;

import org.proteored.miapeapi.interfaces.ms.Analyser;
import org.proteored.miapeapi.xml.ms.autogenerated.MSAnalyzer;
import org.proteored.miapeapi.xml.ms.util.MsControlVocabularyXmlFactory;

public class AnalyzerImpl implements Analyser {
	private final MSAnalyzer analyzer;

	public AnalyzerImpl(MSAnalyzer msIonTrap) {
		this.analyzer = msIonTrap;
	}

	@Override
	public String getName() {
		return MsControlVocabularyXmlFactory.getName(this.analyzer.getName());
	}

	@Override
	public String getReflectron() {
		return MsControlVocabularyXmlFactory.getName(analyzer.getReflectron());
	}

	@Override
	public String getDescription() {
		return this.analyzer.getDescription();
	}

}
