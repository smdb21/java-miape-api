package org.proteored.miapeapi.xml.ms;

import org.proteored.miapeapi.interfaces.Software;
import org.proteored.miapeapi.xml.ms.autogenerated.MIAPESoftwareType;
import org.proteored.miapeapi.xml.ms.util.MsControlVocabularyXmlFactory;

public class MIAPESoftwareImpl implements Software {
	private final MIAPESoftwareType miapeSoftware;

	public MIAPESoftwareImpl(MIAPESoftwareType miapeSoftware) {
		this.miapeSoftware = miapeSoftware;
	}

	@Override
	public String getCatalogNumber() {
		return this.miapeSoftware.getCatalogNumber();
	}

	@Override
	public String getComments() {
		return this.miapeSoftware.getComments();
	}

	@Override
	public String getCustomizations() {
		return this.miapeSoftware.getCustomizations();
	}

	@Override
	public String getDescription() {
		return MsControlVocabularyXmlFactory.getName(this.miapeSoftware.getDescription());
	}

	@Override
	public String getManufacturer() {
		return this.miapeSoftware.getManufacturer();
	}

	@Override
	public String getModel() {
		return this.miapeSoftware.getModel();
	}

	@Override
	public String getName() {
		if (miapeSoftware == null)
			return null;
		return MsControlVocabularyXmlFactory.getName(miapeSoftware.getName());
	}

	@Override
	public String getParameters() {
		return this.miapeSoftware.getParameters();
	}

	@Override
	public String getURI() {
		return this.miapeSoftware.getURI();
	}

	@Override
	public String getVersion() {
		return this.miapeSoftware.getVersion();
	}

	@Override
	public int getId() {
		return -1;
	}

}
