package org.proteored.miapeapi.xml.ms;

import org.proteored.miapeapi.interfaces.ms.Maldi;
import org.proteored.miapeapi.xml.ms.autogenerated.MSMALDI;
import org.proteored.miapeapi.xml.ms.util.MsControlVocabularyXmlFactory;

public class MaldiImpl implements Maldi {
	private final MSMALDI msMaldi;

	public MaldiImpl(MSMALDI msMaldi) {
		this.msMaldi = msMaldi;
	}

	@Override
	public String getExtraction() {
		return this.msMaldi.getExtraction();
	}

	@Override
	public String getLaser() {
		if (this.msMaldi == null)
			return null;
		return MsControlVocabularyXmlFactory.getName(this.msMaldi.getLaser());
	}

	@Override
	public String getLaserWaveLength() {
		return this.msMaldi.getWaveLength();
	}

	@Override
	public String getMatrix() {
		return this.msMaldi.getMatrix();
	}

	@Override
	public String getName() {
		return this.msMaldi.getName();
	}

	@Override
	public String getLaserParameters() {
		return this.msMaldi.getOparameters();
	}

	@Override
	public String getPlateType() {
		return this.msMaldi.getPlateType();
	}

	@Override
	public String getDissociation() {
		return MsControlVocabularyXmlFactory.getName(this.msMaldi.getDissociation());
	}

	@Override
	public String getDissociationSummary() {
		return this.msMaldi.getDissociationSummary();
	}

}
