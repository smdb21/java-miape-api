package org.proteored.miapeapi.xml.ms.adapter;

import org.proteored.miapeapi.cv.ms.IonSourceName;
import org.proteored.miapeapi.interfaces.Adapter;
import org.proteored.miapeapi.interfaces.ms.Other_IonSource;
import org.proteored.miapeapi.xml.ms.autogenerated.MSOtherIonSource;
import org.proteored.miapeapi.xml.ms.autogenerated.ObjectFactory;
import org.proteored.miapeapi.xml.ms.util.MsControlVocabularyXmlFactory;

public class OtherIonSourceAdapter implements Adapter<MSOtherIonSource> {
	private final Other_IonSource ionSource;
	private final ObjectFactory factory;
	private final MsControlVocabularyXmlFactory cvFactory;

	public OtherIonSourceAdapter(Other_IonSource ionSource, ObjectFactory factory,
			MsControlVocabularyXmlFactory cvFactory) {
		this.cvFactory = cvFactory;
		this.factory = factory;
		this.ionSource = ionSource;
	}

	@Override
	public MSOtherIonSource adapt() {
		MSOtherIonSource xmlIonSources = factory.createMSOtherIonSource();
		xmlIonSources.setName(cvFactory.createCV(this.ionSource.getName(), null,
				IonSourceName.getInstance(cvFactory.getCvManager())));
		xmlIonSources.setParameters(this.ionSource.getParameters());
		return xmlIonSources;
	}

}
