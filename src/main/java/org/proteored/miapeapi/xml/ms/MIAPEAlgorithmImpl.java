package org.proteored.miapeapi.xml.ms;

import org.proteored.miapeapi.interfaces.Algorithm;
import org.proteored.miapeapi.xml.ms.autogenerated.MIAPEAlgorithmType;
import org.proteored.miapeapi.xml.ms.util.MsControlVocabularyXmlFactory;

public class MIAPEAlgorithmImpl implements Algorithm {
	private final MIAPEAlgorithmType miapeAlgorithm;
	public MIAPEAlgorithmImpl(MIAPEAlgorithmType miapeAlgorithm) {
		this.miapeAlgorithm = miapeAlgorithm;
	}
	
	@Override
	public String getCatalogNumber() {
		return this.miapeAlgorithm.getCatalogNumber();
	}

	@Override
	public String getComments() {
		return this.miapeAlgorithm.getComments();
	}

	@Override
	public String getDescription() {
		return this.miapeAlgorithm.getDescription();
	}

	@Override
	public String getManufacturer() {
		return this.miapeAlgorithm.getManufacturer();
	}

	@Override
	public String getModel() {
		return this.miapeAlgorithm.getModel();
	}

	@Override
	public String getName() {
		if (miapeAlgorithm == null) return null;	
		return MsControlVocabularyXmlFactory.getName(miapeAlgorithm.getName());
	}

	@Override
	public String getParameters() {
		return this.miapeAlgorithm.getParameters();
	}

	@Override
	public String getURI() {
		return this.miapeAlgorithm.getURI();
	}

	@Override
	public String getVersion() {
		return this.miapeAlgorithm.getVersion();
	}

}
