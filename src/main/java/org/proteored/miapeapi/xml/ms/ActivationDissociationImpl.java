package org.proteored.miapeapi.xml.ms;

import org.proteored.miapeapi.interfaces.ms.ActivationDissociation;
import org.proteored.miapeapi.xml.ms.autogenerated.MSActivationDissociation;
import org.proteored.miapeapi.xml.ms.util.MsControlVocabularyXmlFactory;

public class ActivationDissociationImpl implements ActivationDissociation {
	private final MSActivationDissociation activationDissociationXML;

	public ActivationDissociationImpl(MSActivationDissociation collisionCell) {
		this.activationDissociationXML = collisionCell;
	}

	@Override
	public String getGasType() {
		return MsControlVocabularyXmlFactory.getName(activationDissociationXML.getGas());
	}

	@Override
	public String getName() {
		return activationDissociationXML.getName();
	}

	@Override
	public String getGasPressure() {
		return MsControlVocabularyXmlFactory.getValue(activationDissociationXML.getPressure());
	}

	@Override
	public String getActivationType() {
		return MsControlVocabularyXmlFactory.getName(activationDissociationXML.getActivationType());
	}

	@Override
	public String getPressureUnit() {
		return MsControlVocabularyXmlFactory.getUnitName(activationDissociationXML.getPressure());
	}

}
