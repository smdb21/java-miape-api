package org.proteored.miapeapi.xml.ms;

import org.proteored.miapeapi.interfaces.ms.MSContact;
import org.proteored.miapeapi.xml.ms.autogenerated.MIAPEContactType;
import org.proteored.miapeapi.xml.ms.util.MsControlVocabularyXmlFactory;

public class ContactImpl implements MSContact {
	private final MIAPEContactType contact;
	public ContactImpl(MIAPEContactType contact) {
		this.contact  = contact;
	}

	@Override
	public String getAddress() {
		return contact.getAddress();
	}

	@Override
	public String getCP() {
		return contact.getCP();
	}

	@Override
	public String getCountry() {
		return contact.getCountry();
	}

	@Override
	public String getDepartment() {
		return contact.getDepartment();
	}

	@Override
	public String getEmail() {
		return contact.getEmail();
	}

	@Override
	public String getFax() {
		return contact.getFax();
	}

	@Override
	public String getInstitution() {
		return contact.getInstitution();
	}

	@Override
	public String getLastName() {
		return contact.getLastName();
	}

	@Override
	public String getLocality() {
		return contact.getLocality();
	}

	@Override
	public String getName() {
		return contact.getName();
	}

	@Override
	public String getTelephone() {
		return contact.getTelephone();
	}

	@Override
	public String getPosition() {
		if (contact.getPosition() == null) return null;	
		return MsControlVocabularyXmlFactory.getName(contact.getPosition());
	}

}
