package org.proteored.miapeapi.xml.ms.adapter;

import org.proteored.miapeapi.cv.AdditionalInformationName;
import org.proteored.miapeapi.interfaces.Adapter;
import org.proteored.miapeapi.interfaces.ms.MSAdditionalInformation;
import org.proteored.miapeapi.xml.ms.autogenerated.ObjectFactory;
import org.proteored.miapeapi.xml.ms.autogenerated.ParamType;
import org.proteored.miapeapi.xml.ms.util.MsControlVocabularyXmlFactory;

public class AdditionalInformationAdapter implements Adapter<ParamType> {
	private final MSAdditionalInformation addInfo;
	private final MsControlVocabularyXmlFactory cvFactory;

	public AdditionalInformationAdapter(MSAdditionalInformation addInfo, ObjectFactory factory,
			MsControlVocabularyXmlFactory cvFactory2) {
		this.addInfo = addInfo;
		this.cvFactory = cvFactory2;
	}

	@Override
	public ParamType adapt() {
		return cvFactory.createCV(this.addInfo.getName(), this.addInfo.getValue(),
				AdditionalInformationName.getInstance(cvFactory.getCvManager()));

	}

}
