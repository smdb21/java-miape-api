package org.proteored.miapeapi.xml.ms;

import org.proteored.miapeapi.interfaces.ms.DataAnalysis;
import org.proteored.miapeapi.xml.ms.autogenerated.MSDataAnalysis;

public class DataAnalysisImpl extends MIAPESoftwareImpl implements DataAnalysis {
	private final MSDataAnalysis xmlDataAnalysis;

	public DataAnalysisImpl(MSDataAnalysis msPeakListGeneration) {
		super(msPeakListGeneration);
		this.xmlDataAnalysis = msPeakListGeneration;
	}

	@Override
	public String getParametersLocation() {
		return this.xmlDataAnalysis.getParametersLocation();
	}

}
