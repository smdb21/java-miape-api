package org.proteored.miapeapi.xml.ms;

import org.proteored.miapeapi.interfaces.ms.Acquisition;
import org.proteored.miapeapi.xml.ms.autogenerated.MSAcquisition;
import org.proteored.miapeapi.xml.ms.util.MsControlVocabularyXmlFactory;

public class AcquisitionImpl implements Acquisition {
	private final MSAcquisition controlAnalysisSoftware;

	public AcquisitionImpl(MSAcquisition controlAnalysisSoftware) {
		this.controlAnalysisSoftware = controlAnalysisSoftware;
	}

	@Override
	public String getParameterFile() {
		return this.controlAnalysisSoftware.getParameterFile();
	}

	@Override
	public String getCatalogNumber() {
		return this.controlAnalysisSoftware.getCatalogNumber();
	}

	@Override
	public String getComments() {
		return this.controlAnalysisSoftware.getComments();
	}

	@Override
	public String getCustomizations() {
		return this.controlAnalysisSoftware.getCustomizations();
	}

	@Override
	public String getDescription() {
		return MsControlVocabularyXmlFactory.getName(this.controlAnalysisSoftware.getDescription());
	}

	@Override
	public String getManufacturer() {
		return this.controlAnalysisSoftware.getManufacturer();
	}

	@Override
	public String getModel() {
		if (this.controlAnalysisSoftware == null)
			return null;
		return controlAnalysisSoftware.getModel();
	}

	@Override
	public String getName() {
		if (this.controlAnalysisSoftware == null)
			return null;
		return MsControlVocabularyXmlFactory.getName(this.controlAnalysisSoftware.getName());
	}

	@Override
	public String getParameters() {
		return this.controlAnalysisSoftware.getParameters();
	}

	@Override
	public String getURI() {
		return this.controlAnalysisSoftware.getURI();
	}

	@Override
	public String getVersion() {
		return this.controlAnalysisSoftware.getVersion();
	}

	@Override
	public int getId() {
		return -1;
	}

	@Override
	public String getTransitionListFile() {
		return this.controlAnalysisSoftware.getTransitionListFile();
	}

	@Override
	public String getTargetList() {
		return this.controlAnalysisSoftware.getTargetList();
	}

}
