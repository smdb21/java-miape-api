package org.proteored.miapeapi.xml.ge;

import java.util.HashSet;
import java.util.List;
import java.util.Map;
import java.util.Set;

import org.proteored.miapeapi.interfaces.ge.Lane;
import org.proteored.miapeapi.interfaces.ge.SampleApplication;
import org.proteored.miapeapi.xml.ge.autogenerated.GELane;
import org.proteored.miapeapi.xml.ge.autogenerated.GESample;
import org.proteored.miapeapi.xml.ge.autogenerated.GESampleApplication;

public class SampleApplicationImpl implements SampleApplication {
	private final GESampleApplication sampleApplicationXML;
	private final Map<String, GESample> sampleMap;

	public SampleApplicationImpl(GESampleApplication geSampleApplication, Map<String, GESample> sampleMap) {
		this.sampleApplicationXML = geSampleApplication;
		this.sampleMap = sampleMap;
	}

	@Override
	public String getDescription() {
		return sampleApplicationXML.getDescription();
	}

	@Override
	public Set<Lane> getLanes() {
		Set<Lane> setofLanes = new HashSet<Lane>();
		List<GELane> laneList = sampleApplicationXML.getGELane();
		for (GELane geLane : laneList) {
			setofLanes.add(new LaneImpl(geLane, this.sampleMap));
		}
		return setofLanes;
	}

	@Override
	public String getName() {
		return sampleApplicationXML.getName();
	}


}
