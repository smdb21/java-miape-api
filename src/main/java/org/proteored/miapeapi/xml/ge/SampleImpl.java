package org.proteored.miapeapi.xml.ge;

import org.proteored.miapeapi.interfaces.ge.Sample;
import org.proteored.miapeapi.xml.ge.autogenerated.GESample;
import org.proteored.miapeapi.xml.util.MiapeXmlUtil;

public class SampleImpl implements Sample {
	private final GESample sampleXML;

	public SampleImpl(GESample sampleXML2) {
		this.sampleXML = sampleXML2;
	}

	@Override
	public String getDescription() {
		return sampleXML.getDescription();
	}


	@Override
	public String getName() {
		return sampleXML.getName();
	}

	@Override
	public int getId() {
		return MiapeXmlUtil.getIdFromXMLId(sampleXML.getId());
	}

}
