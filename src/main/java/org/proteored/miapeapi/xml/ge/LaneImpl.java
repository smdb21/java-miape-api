package org.proteored.miapeapi.xml.ge;

import java.util.Map;

import org.proteored.miapeapi.interfaces.ge.Lane;
import org.proteored.miapeapi.interfaces.ge.Sample;
import org.proteored.miapeapi.xml.ge.autogenerated.GELane;
import org.proteored.miapeapi.xml.ge.autogenerated.GESample;
import org.proteored.miapeapi.xml.util.MiapeXmlUtil;

public class LaneImpl implements Lane {
	private final GELane laneXML;

	private final Map<String, GESample> mapSample;
	public LaneImpl(GELane geLane, Map<String, GESample> map) {
		this.laneXML = geLane;
		this.mapSample = map;
	}

	@Override
	public String getDescription() {
		return laneXML.getDescription();
	}

	@Override
	public String getLaneNumber() {
		return laneXML.getLaneNumber();
	}

	@Override
	public String getName() {
		return  laneXML.getName();
	}

	@Override
	public Sample getReferencedSample() {
		if (mapSample.containsKey(laneXML.getSampleRef())){
			return new SampleImpl(mapSample.get(laneXML.getSampleRef()));
		}
		return null;

	}

	@Override
	public int getId() {
		return MiapeXmlUtil.getIdFromXMLId(laneXML.getId());
	}

}
