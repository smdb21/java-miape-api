package org.proteored.miapeapi.xml.ge;

import org.proteored.miapeapi.interfaces.Substance;
import org.proteored.miapeapi.xml.ge.autogenerated.MIAPESubstanceType;
import org.proteored.miapeapi.xml.ge.util.GEControlVocabularyXmlFactory;
import org.proteored.miapeapi.xml.util.MiapeXmlUtil;

public class SubstanceImpl implements Substance {
	protected final MIAPESubstanceType substanceXML;

	public SubstanceImpl(MIAPESubstanceType substanceItem) {
		this.substanceXML = substanceItem;
	}


	@Override
	public String getConcentration() {
		return GEControlVocabularyXmlFactory.getValue(substanceXML.getConcentration());
	}

	@Override
	public String getConcentrationUnit() {
		return GEControlVocabularyXmlFactory.getUnitName(substanceXML.getConcentration());
	}

	@Override
	public String getDescription() {
		return substanceXML.getDescription();
	}

	@Override
	public String getMass() {
		return GEControlVocabularyXmlFactory.getValue(substanceXML.getMass());
	}

	@Override
	public String getMassUnit() {
		return GEControlVocabularyXmlFactory.getUnitName(substanceXML.getMass());
	}

	@Override
	public String getName() {
		return GEControlVocabularyXmlFactory.getName(substanceXML.getName());

	}

	@Override
	public String getType() {
		return substanceXML.getType();
	}

	@Override
	public String getVolume() {
		return GEControlVocabularyXmlFactory.getValue(substanceXML.getVolume());
	}

	@Override
	public String getVolumeUnit() {
		return GEControlVocabularyXmlFactory.getUnitName(substanceXML.getVolume());
	}


	@Override
	public int getId() {
		return MiapeXmlUtil.getIdFromXMLId(substanceXML.getId());
	}



}
