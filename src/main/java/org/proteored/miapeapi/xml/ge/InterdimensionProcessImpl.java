package org.proteored.miapeapi.xml.ge;

import java.util.List;
import java.util.Set;

import org.proteored.miapeapi.interfaces.Buffer;
import org.proteored.miapeapi.interfaces.Equipment;
import org.proteored.miapeapi.interfaces.ge.Agent;
import org.proteored.miapeapi.interfaces.ge.InterdimensionProcess;
import org.proteored.miapeapi.xml.ge.autogenerated.GEInterDimensionProcess;
import org.proteored.miapeapi.xml.ge.autogenerated.MIAPEBufferType;
import org.proteored.miapeapi.xml.ge.autogenerated.MIAPEGEEquipmentType;
import org.proteored.miapeapi.xml.ge.autogenerated.MIAPESubstanceType;

import gnu.trove.set.hash.THashSet;

public class InterdimensionProcessImpl implements InterdimensionProcess {
	private final GEInterDimensionProcess interdimensionXML;

	public InterdimensionProcessImpl(GEInterDimensionProcess geInterDimensionProcess) {
		this.interdimensionXML = geInterDimensionProcess;
	}

	@Override
	public Set<Buffer> getInterdimensionBuffers() {
		Set<Buffer> setOfBuffers = new THashSet<Buffer>();
		List<MIAPEBufferType> bufferList = interdimensionXML.getGEInterDimensionBuffer();
		for (MIAPEBufferType miapeBufferItem : bufferList) {
			setOfBuffers.add(new BufferImpl(miapeBufferItem));
		}
		return setOfBuffers;
	}

	@Override
	public Set<Equipment> getInterdimensionEquipments() {
		Set<Equipment> setOfEquipments = new THashSet<Equipment>();
		List<MIAPEGEEquipmentType> equipmentList = interdimensionXML.getGEInterDimensionEquipment();
		for (MIAPEGEEquipmentType miapeEquipmentItem : equipmentList) {
			setOfEquipments.add(new EquipmentImpl(miapeEquipmentItem));
		}
		return setOfEquipments;
	}

	@Override
	public Set<Agent> getInterdimensionReagents() {
		Set<Agent> setOfSubstances = new THashSet<Agent>();
		List<MIAPESubstanceType> bufferList = interdimensionXML.getGEInterDimensionReagent();
		for (MIAPESubstanceType miapeSubstanceItem : bufferList) {
			setOfSubstances.add(new AgentImpl(miapeSubstanceItem));
		}
		return setOfSubstances;
	}

	@Override
	public String getName() {
		return interdimensionXML.getName();
	}

	@Override
	public String getProtocol() {
		return interdimensionXML.getProtocol();
	}

}
