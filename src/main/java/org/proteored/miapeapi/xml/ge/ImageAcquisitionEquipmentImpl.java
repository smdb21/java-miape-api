package org.proteored.miapeapi.xml.ge;

import org.proteored.miapeapi.interfaces.ge.ImageAcquisitionEquipment;
import org.proteored.miapeapi.xml.ge.autogenerated.GEImageAcquisitionEquipmentType;
import org.proteored.miapeapi.xml.ge.util.GEControlVocabularyXmlFactory;

public class ImageAcquisitionEquipmentImpl extends EquipmentImpl implements ImageAcquisitionEquipment {
	private final GEImageAcquisitionEquipmentType imageAquisitionEquipmentXML;

	public ImageAcquisitionEquipmentImpl(
			GEImageAcquisitionEquipmentType imageAcquisitionEquipmentItem) {
		super(imageAcquisitionEquipmentItem);
		this.imageAquisitionEquipmentXML = imageAcquisitionEquipmentItem;
	}

	@Override
	public Integer getCalibration() {
		return this.imageAquisitionEquipmentXML.getCalibration(); 
	}

	@Override
	public String getParametersUrl() {
		return this.imageAquisitionEquipmentXML.getParameters();
	}

	@Override
	public String getType() {
		return GEControlVocabularyXmlFactory.getName(this.imageAquisitionEquipmentXML.getType());
	}


}
