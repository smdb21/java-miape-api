package org.proteored.miapeapi.xml.ge.adapter;

import org.proteored.miapeapi.interfaces.Adapter;
import org.proteored.miapeapi.interfaces.ge.Sample;
import org.proteored.miapeapi.xml.ge.autogenerated.GESample;
import org.proteored.miapeapi.xml.ge.autogenerated.ObjectFactory;
import org.proteored.miapeapi.xml.util.MiapeXmlUtil;

public class SampleAdapter implements Adapter<GESample> {
	private final ObjectFactory factory;
	private final Sample sample;

	public SampleAdapter(ObjectFactory factory, Sample sample){
		this.factory = factory;
		this.sample = sample;
	}

	@Override
	public GESample adapt() {
		GESample sampleXML = factory.createGESample();
		sampleXML.setDescription(sample.getDescription());
		sampleXML.setName(sample.getName());
		sampleXML.setId(MiapeXmlUtil.IdentifierPrefixes.SAMPLE.getPrefix() + sample.getId());

		return sampleXML;
	}

}
