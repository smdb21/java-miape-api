package org.proteored.miapeapi.xml.msi;

import org.proteored.miapeapi.interfaces.msi.PostProcessingMethod;
import org.proteored.miapeapi.xml.msi.autogenerated.MIAPEAlgorithmType;
import org.proteored.miapeapi.xml.msi.util.MSIControlVocabularyXmlFactory;

public class ValidationAlgorithmImpl extends AlgorithmImpl implements PostProcessingMethod {
	private final MIAPEAlgorithmType validationMethodXML;

	public ValidationAlgorithmImpl(MIAPEAlgorithmType xmlAlgorithm) {
		super(xmlAlgorithm);
		this.validationMethodXML = xmlAlgorithm;
	}

	@Override
	public String getName() {
		return MSIControlVocabularyXmlFactory.getName(validationMethodXML.getName());
	}
}
