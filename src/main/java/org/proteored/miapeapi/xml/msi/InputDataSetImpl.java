package org.proteored.miapeapi.xml.msi;

import java.util.HashSet;
import java.util.Set;

import org.proteored.miapeapi.interfaces.msi.InputData;
import org.proteored.miapeapi.interfaces.msi.InputDataSet;
import org.proteored.miapeapi.xml.msi.autogenerated.MSIInputData;
import org.proteored.miapeapi.xml.msi.autogenerated.MSIInputDataSet;
import org.proteored.miapeapi.xml.util.MiapeXmlUtil;

public class InputDataSetImpl implements InputDataSet {
	private final MSIInputDataSet inputDataSetXML;
	public InputDataSetImpl(MSIInputDataSet xmlInputDataSet) {
		this.inputDataSetXML = xmlInputDataSet;
	}

	@Override
	public Set<InputData> getInputDatas() {
		Set<InputData> setOfInputData = new HashSet<InputData>();
		if (inputDataSetXML.getMSIInputData() != null) {
			for (MSIInputData inputDataXML : inputDataSetXML.getMSIInputData()) {
				setOfInputData.add(new InputDataImpl(inputDataXML));
			}
			return setOfInputData;
		}
		return null;
	}

	@Override
	public String getName() {
		return inputDataSetXML.getName();
	}

	@Override
	public int getId() {
		return MiapeXmlUtil.getIdFromXMLId(inputDataSetXML.getId());
	}

}
