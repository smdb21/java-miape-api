package org.proteored.miapeapi.xml.msi;

import org.proteored.miapeapi.interfaces.msi.InputData;
import org.proteored.miapeapi.xml.msi.autogenerated.MSIInputData;
import org.proteored.miapeapi.xml.msi.util.MSIControlVocabularyXmlFactory;
import org.proteored.miapeapi.xml.util.MiapeXmlUtil;

public class InputDataImpl implements InputData {
	private final MSIInputData xmlInputData;
	public InputDataImpl(MSIInputData xmlInputData) {
		this.xmlInputData = xmlInputData;

	}

	@Override
	public String getDescription() {
		return xmlInputData.getDescription();
	}

	@Override
	public String getMSFileType() {
		return MSIControlVocabularyXmlFactory.getName(xmlInputData.getMSFileType());
	}

	@Override
	public String getName() {
		return xmlInputData.getName();
	}

	@Override
	public String getSourceDataUrl() {
		return xmlInputData.getSourceDataURL();
	}

	@Override
	public int getId() {
		return MiapeXmlUtil.getIdFromXMLId(xmlInputData.getId());
	}



}
