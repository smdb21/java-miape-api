package org.proteored.miapeapi.xml.msi;

import org.proteored.miapeapi.interfaces.msi.PeptideScore;
import org.proteored.miapeapi.xml.msi.autogenerated.ParamType;
import org.proteored.miapeapi.xml.msi.util.MSIControlVocabularyXmlFactory;

public class PeptideScoreImpl implements PeptideScore {
	private final ParamType xmlParamType;

	public PeptideScoreImpl(ParamType xmlScore) {
		this.xmlParamType = xmlScore;
	}

	@Override
	public String getName() {

		return MSIControlVocabularyXmlFactory.getName(xmlParamType);

	}

	@Override
	public String getValue() {

		return MSIControlVocabularyXmlFactory.getValue(xmlParamType);

	}

}
