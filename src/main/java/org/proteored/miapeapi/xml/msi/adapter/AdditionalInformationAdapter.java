package org.proteored.miapeapi.xml.msi.adapter;

import org.proteored.miapeapi.cv.AdditionalInformationName;
import org.proteored.miapeapi.interfaces.Adapter;
import org.proteored.miapeapi.interfaces.msi.MSIAdditionalInformation;
import org.proteored.miapeapi.xml.msi.autogenerated.ObjectFactory;
import org.proteored.miapeapi.xml.msi.autogenerated.ParamType;
import org.proteored.miapeapi.xml.msi.util.MSIControlVocabularyXmlFactory;

public class AdditionalInformationAdapter implements Adapter<ParamType> {
	private final MSIAdditionalInformation addInfo;
	private final ObjectFactory factory;
	private final MSIControlVocabularyXmlFactory cvFactory;

	public AdditionalInformationAdapter(MSIAdditionalInformation addInfo, ObjectFactory factory,
			MSIControlVocabularyXmlFactory cvFactory2) {
		this.addInfo = addInfo;
		this.cvFactory = cvFactory2;
		this.factory = factory;
	}

	@Override
	public ParamType adapt() {
		return cvFactory.createCV(this.addInfo.getName(), this.addInfo.getValue(),
				AdditionalInformationName.getInstance(cvFactory.getCvManager()));
	}

}
