package org.proteored.miapeapi.xml.msi;

import org.proteored.miapeapi.interfaces.Algorithm;
import org.proteored.miapeapi.xml.msi.autogenerated.MIAPEAlgorithmType;
import org.proteored.miapeapi.xml.msi.util.MSIControlVocabularyXmlFactory;

public class AlgorithmImpl implements Algorithm {
	private final MIAPEAlgorithmType xmlScoringAlgorithm;
	public AlgorithmImpl(MIAPEAlgorithmType xmlScoringAlgorithm) {
		this.xmlScoringAlgorithm = xmlScoringAlgorithm;
	}

	@Override
	public String getCatalogNumber() {
		return xmlScoringAlgorithm.getCatalogNumber();
	}

	@Override
	public String getComments() {
		return xmlScoringAlgorithm.getComments();
	}

	@Override
	public String getDescription() {
		return xmlScoringAlgorithm.getDescription();
	}

	@Override
	public String getManufacturer() {
		return xmlScoringAlgorithm.getManufacturer();
	}

	@Override
	public String getModel() {
		return xmlScoringAlgorithm.getModel();
	}

	@Override
	public String getName() {
		return MSIControlVocabularyXmlFactory.getName(xmlScoringAlgorithm.getName());
	}

	@Override
	public String getParameters() {
		return xmlScoringAlgorithm.getParameters();
	}

	@Override
	public String getURI() {
		return xmlScoringAlgorithm.getURI();

	}

	@Override
	public String getVersion() {
		return xmlScoringAlgorithm.getVersion();
	}

}
