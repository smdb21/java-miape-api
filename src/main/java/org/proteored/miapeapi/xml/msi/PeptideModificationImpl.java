package org.proteored.miapeapi.xml.msi;

import org.proteored.miapeapi.interfaces.msi.PeptideModification;
import org.proteored.miapeapi.xml.msi.autogenerated.MSIPeptideModification;
import org.proteored.miapeapi.xml.msi.util.MSIControlVocabularyXmlFactory;

public class PeptideModificationImpl implements PeptideModification {

	private final MSIPeptideModification xmlModification;

	public PeptideModificationImpl(MSIPeptideModification xmlModification) {
		this.xmlModification = xmlModification;
	}

	@Override
	public Double getAvgDelta() {
		return xmlModification.getAvgDelta();
	}

	@Override
	public Double getMonoDelta() {
		return xmlModification.getMonoDelta();
	}

	@Override
	public String getName() {
		return MSIControlVocabularyXmlFactory.getName(xmlModification.getName());
	}

	@Override
	public int getPosition() {

		return xmlModification.getPosition();

	}

	@Override
	public String getResidues() {
		return xmlModification.getResidues();
	}

	@Override
	public String getReplacementResidue() {
		return xmlModification.getReplacementResidue();
	}

	@Override
	public Double getNeutralLoss() {
		return xmlModification.getNeutralLoss();
	}

	@Override
	public String getModificationEvidence() {
		return xmlModification.getEvidence();
	}

}
