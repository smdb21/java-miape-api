package org.proteored.miapeapi.xml.msi;

import org.proteored.miapeapi.interfaces.ms.MSContact;
import org.proteored.miapeapi.xml.msi.autogenerated.MIAPEContactType;
import org.proteored.miapeapi.xml.msi.util.MSIControlVocabularyXmlFactory;

public class ContactImpl implements MSContact {
	private final MIAPEContactType xmlContact;
	public ContactImpl(MIAPEContactType xmlContact) {
		this.xmlContact = xmlContact;
	}

	@Override
	public String getAddress() {
		return xmlContact.getAddress();
	}

	@Override
	public String getCP() {
		return xmlContact.getCP();
	}

	@Override
	public String getCountry() {
		return xmlContact.getCountry();
	}

	@Override
	public String getDepartment() {
		return xmlContact.getDepartment();
	}

	@Override
	public String getEmail() {
		return xmlContact.getEmail();
	}

	@Override
	public String getFax() {
		return xmlContact.getFax();
	}

	@Override
	public String getInstitution() {
		return xmlContact.getInstitution();
	}

	@Override
	public String getLastName() {
		return xmlContact.getLastName();
	}

	@Override
	public String getLocality() {
		return xmlContact.getLocality();
	}

	@Override
	public String getName() {
		return xmlContact.getName();
	}

	@Override
	public String getPosition() {
		if (xmlContact.getPosition() != null) {
			return MSIControlVocabularyXmlFactory.getName(xmlContact.getPosition());
		}
		return null;
	}

	@Override
	public String getTelephone() {
		return xmlContact.getTelephone();
	}

}
