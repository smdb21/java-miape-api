package org.proteored.miapeapi.xml.mzidentml;

import org.proteored.miapeapi.cv.ControlVocabularyTerm;
import org.proteored.miapeapi.interfaces.msi.PeptideScore;
import org.proteored.miapeapi.xml.mzidentml.autogenerated.FuGECommonOntologyParamType;

public class PeptideScoreImpl implements PeptideScore {
	private final String name;
	private final String value;

	public PeptideScoreImpl(FuGECommonOntologyParamType param) {
		name = param.getName();
		value = param.getValue();

	}

	public PeptideScoreImpl(ControlVocabularyTerm cvTerm, String value) {
		name = cvTerm.getPreferredName();
		this.value = value;

	}

	@Override
	public String getName() {
		return name;
	}

	@Override
	public String getValue() {
		return value;
	}

}
