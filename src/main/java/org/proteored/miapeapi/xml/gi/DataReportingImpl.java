package org.proteored.miapeapi.xml.gi;

import org.proteored.miapeapi.interfaces.gi.DataReporting;
import org.proteored.miapeapi.xml.gi.autogenerated.GIDataReporting;

public class DataReportingImpl implements DataReporting {
	private final GIDataReporting xmlDataReporting;
	public DataReportingImpl(GIDataReporting xmlDataReporting) {
		this.xmlDataReporting = xmlDataReporting;
	}



	@Override
	public String getFeatureList() {
		return xmlDataReporting.getFeatureList();
	}

	@Override
	public String getFeatureURI() {
		return xmlDataReporting.getFeatureURI();
	}

	@Override
	public String getMatchesList() {
		return xmlDataReporting.getMatchesList();
	}

	@Override
	public String getMatchesURI() {
		return xmlDataReporting.getMatchesURI();
	}

	@Override
	public String getName() {
		return xmlDataReporting.getName();
	}

	@Override
	public String getResultsDescription() {
		return xmlDataReporting.getResultsDescription();
	}

	@Override
	public String getResultsURI() {
		return xmlDataReporting.getResultsURI();
	}


}
