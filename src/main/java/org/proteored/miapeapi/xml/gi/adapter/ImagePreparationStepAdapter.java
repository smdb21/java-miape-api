package org.proteored.miapeapi.xml.gi.adapter;

import org.proteored.miapeapi.interfaces.Adapter;
import org.proteored.miapeapi.interfaces.gi.ImagePreparationStep;
import org.proteored.miapeapi.xml.gi.autogenerated.GIImagePreparationStep;
import org.proteored.miapeapi.xml.gi.autogenerated.ObjectFactory;
import org.proteored.miapeapi.xml.gi.util.GIControlVocabularyXmlFactory;

public class ImagePreparationStepAdapter implements Adapter<GIImagePreparationStep> {
	private final ImagePreparationStep preparation;
	private final ObjectFactory factory;
	private final GIControlVocabularyXmlFactory cvFactory;
	public ImagePreparationStepAdapter(ImagePreparationStep preparation,
			ObjectFactory factory, GIControlVocabularyXmlFactory cvFactory) {
		this.preparation = preparation;
		this.factory = factory;
		this.cvFactory = cvFactory;
	}

	@Override
	public GIImagePreparationStep adapt() {
		GIImagePreparationStep xmlPreparation = factory.createGIImagePreparationStep();
		xmlPreparation.setName(preparation.getName());
		xmlPreparation.setParameters(preparation.getParameters());
		xmlPreparation.setStepOrder(preparation.getStepOrder());
		xmlPreparation.setType(preparation.getType());
		return xmlPreparation;
	}

}
