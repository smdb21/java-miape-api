package org.proteored.miapeapi.xml.gi.adapter;

import org.proteored.miapeapi.cv.AdditionalInformationName;
import org.proteored.miapeapi.interfaces.Adapter;
import org.proteored.miapeapi.interfaces.gi.GIAdditionalInformation;
import org.proteored.miapeapi.xml.gi.autogenerated.ObjectFactory;
import org.proteored.miapeapi.xml.gi.autogenerated.ParamType;
import org.proteored.miapeapi.xml.gi.util.GIControlVocabularyXmlFactory;

public class AdditionalInformationAdapter implements Adapter<ParamType> {
	private final GIAdditionalInformation addInfo;
	private final ObjectFactory factory;
	private final GIControlVocabularyXmlFactory cvFactory;

	public AdditionalInformationAdapter(GIAdditionalInformation addInfo, ObjectFactory factory,
			GIControlVocabularyXmlFactory cvFactory2) {
		this.addInfo = addInfo;
		this.cvFactory = cvFactory2;
		this.factory = factory;
	}

	@Override
	public ParamType adapt() {

		return cvFactory.createCV(this.addInfo.getName(), this.addInfo.getValue(),
				AdditionalInformationName.getInstance(cvFactory.getCvManager()));
	}
}
