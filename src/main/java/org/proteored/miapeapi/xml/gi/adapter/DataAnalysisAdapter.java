package org.proteored.miapeapi.xml.gi.adapter;

import org.proteored.miapeapi.interfaces.Adapter;
import org.proteored.miapeapi.interfaces.gi.DataAnalysis;
import org.proteored.miapeapi.xml.gi.autogenerated.GIDataAnalysis;
import org.proteored.miapeapi.xml.gi.autogenerated.ObjectFactory;
import org.proteored.miapeapi.xml.gi.util.GIControlVocabularyXmlFactory;

public class DataAnalysisAdapter implements Adapter<GIDataAnalysis> {
	private final DataAnalysis dataAnalysis;
	private final GIControlVocabularyXmlFactory cvFactory;
	private final ObjectFactory factory;
	 
	public DataAnalysisAdapter(DataAnalysis dataAnalysis,
			ObjectFactory factory, GIControlVocabularyXmlFactory cvFactory) {
		this.dataAnalysis = dataAnalysis;
		this.cvFactory = cvFactory;
		this.factory = factory;
	}

	 

	@Override
	public GIDataAnalysis adapt() {
		GIDataAnalysis xmlDataAnalysis = factory.createGIDataAnalysis();
		xmlDataAnalysis.setInputData(dataAnalysis.getInputData());
		xmlDataAnalysis.setIntent(dataAnalysis.getIntent());
		xmlDataAnalysis.setName(dataAnalysis.getName());
		xmlDataAnalysis.setParameters(dataAnalysis.getParameters());
		xmlDataAnalysis.setType(dataAnalysis.getType());
		return xmlDataAnalysis;
	}

}
