package org.proteored.miapeapi.xml.gi;

import org.proteored.miapeapi.interfaces.gi.ImageGelInformatics;
import org.proteored.miapeapi.xml.gi.autogenerated.GIImage;
import org.proteored.miapeapi.xml.gi.util.GIControlVocabularyXmlFactory;
import org.proteored.miapeapi.xml.util.MiapeXmlUtil;

public class ImageImpl implements ImageGelInformatics {
	private final GIImage imageXML;

	public ImageImpl(GIImage imageXML) {
		this.imageXML = imageXML;
	}

	@Override
	public String getType() {
		return imageXML.getType();
	}

	@Override
	public String getBitDepth() {
		return imageXML.getBitDepth();
	}

	@Override
	public String getDimensionUnit() {
		if (imageXML.getDimensionX() != null)
			return GIControlVocabularyXmlFactory.getUnitName(imageXML.getDimensionX());
		if (imageXML.getDimensionY() != null)
			return GIControlVocabularyXmlFactory.getUnitName(imageXML.getDimensionY());
		return null;
	}

	@Override
	public String getDimensionX() {
		return GIControlVocabularyXmlFactory.getValue(imageXML.getDimensionX());
	}

	@Override
	public String getDimensionY() {
		return GIControlVocabularyXmlFactory.getValue(imageXML.getDimensionY());
	}

	@Override
	public String getFormat() {
		return GIControlVocabularyXmlFactory.getName(imageXML.getFormat());
	}

	@Override
	public int getId() {
		return MiapeXmlUtil.getIdFromXMLId(imageXML.getId());
	}

	@Override
	public String getLocation() {
		return imageXML.getLocation();
	}

	@Override
	public String getName() {
		return imageXML.getName();
	}

	@Override
	public String getOrientation() {
		return imageXML.getOrientation();
	}

	@Override
	public String getResolution() {
		return GIControlVocabularyXmlFactory.getValue(imageXML.getResolution());
	}

	@Override
	public String getResolutionUnit() {
		return GIControlVocabularyXmlFactory.getUnitName(imageXML.getResolution());
	}

}
