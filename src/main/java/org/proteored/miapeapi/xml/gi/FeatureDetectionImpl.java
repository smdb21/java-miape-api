package org.proteored.miapeapi.xml.gi;

import org.proteored.miapeapi.interfaces.gi.FeatureDetection;
import org.proteored.miapeapi.xml.gi.autogenerated.GIFeatureDetectionType;

public class FeatureDetectionImpl extends AlgorithmImpl implements FeatureDetection {
	GIFeatureDetectionType featureDetectionXML ;

	public FeatureDetectionImpl(GIFeatureDetectionType featureDetectionXML) {
		super(featureDetectionXML);
		this.featureDetectionXML = featureDetectionXML;

	}

	@Override
	public String getEditing() {
		return featureDetectionXML.getEditing();
	}

	@Override
	public String getStepOrder() {
		return featureDetectionXML.getStepOrder();
	}
}
