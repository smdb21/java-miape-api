package org.proteored.miapeapi.xml.gi;

import org.proteored.miapeapi.interfaces.Software;
import org.proteored.miapeapi.xml.gi.autogenerated.MIAPESoftwareType;
import org.proteored.miapeapi.xml.gi.util.GIControlVocabularyXmlFactory;
import org.proteored.miapeapi.xml.gi.util.Utils;

public class SoftwareImpl implements Software {
	protected MIAPESoftwareType xmlSoftware;

	public SoftwareImpl(MIAPESoftwareType xmlSoftware) {
		this.xmlSoftware = xmlSoftware;
	}

	@Override
	public String getCatalogNumber() {
		return xmlSoftware.getCatalogNumber();
	}

	@Override
	public String getComments() {
		return xmlSoftware.getComments();
	}

	@Override
	public String getCustomizations() {
		return xmlSoftware.getCustomizations();
	}

	@Override
	public String getDescription() {
		return GIControlVocabularyXmlFactory.getName(this.xmlSoftware.getDescription());
	}

	@Override
	public String getManufacturer() {
		return xmlSoftware.getManufacturer();
	}

	@Override
	public String getModel() {
		return xmlSoftware.getModel();
	}

	@Override
	public String getName() {
		return Utils.writeParam(xmlSoftware.getName());
	}

	@Override
	public String getParameters() {
		return xmlSoftware.getParameters();
	}

	@Override
	public String getURI() {
		return xmlSoftware.getURI();
	}

	@Override
	public String getVersion() {
		return xmlSoftware.getVersion();
	}

	@Override
	public int getId() {
		return -1;
	}

}
