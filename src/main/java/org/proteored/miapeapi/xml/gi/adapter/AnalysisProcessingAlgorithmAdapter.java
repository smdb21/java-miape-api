package org.proteored.miapeapi.xml.gi.adapter;

import org.proteored.miapeapi.interfaces.gi.ImageProcessingStep;
import org.proteored.miapeapi.xml.gi.autogenerated.GIImageProcessingAlgorithmType;
import org.proteored.miapeapi.xml.gi.autogenerated.ObjectFactory;
import org.proteored.miapeapi.xml.gi.util.GIControlVocabularyXmlFactory;

public class AnalysisProcessingAlgorithmAdapter extends AlgorithmAdapter{
	private final ImageProcessingStep step;
	private final ObjectFactory factory;

	public AnalysisProcessingAlgorithmAdapter(ImageProcessingStep step, ObjectFactory factory, GIControlVocabularyXmlFactory cvFactory) {
		super(step, factory, cvFactory);
		this.step = step;
		this.factory = factory;
	}

	@Override
	public GIImageProcessingAlgorithmType adapt() {
		GIImageProcessingAlgorithmType xmlProcessingAlgorithm = factory.createGIImageProcessingAlgorithmType();
		xmlProcessingAlgorithm.setStepOrder(step.getStepOrder());
		return xmlProcessingAlgorithm;
	}



}
