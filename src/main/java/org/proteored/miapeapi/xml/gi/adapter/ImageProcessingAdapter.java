package org.proteored.miapeapi.xml.gi.adapter;

import org.proteored.miapeapi.interfaces.Adapter;
import org.proteored.miapeapi.interfaces.gi.ImageProcessing;
import org.proteored.miapeapi.xml.gi.autogenerated.GIImageProcessing;
import org.proteored.miapeapi.xml.gi.autogenerated.ObjectFactory;
import org.proteored.miapeapi.xml.gi.util.GIControlVocabularyXmlFactory;

public class ImageProcessingAdapter implements Adapter<GIImageProcessing>{
	private final ImageProcessing processing;
	private final ObjectFactory factory;
	public ImageProcessingAdapter(ImageProcessing processing,
			ObjectFactory factory, GIControlVocabularyXmlFactory cvFactory) {
		this.processing = processing;
		this.factory = factory;

	}

	@Override
	public GIImageProcessing adapt() {
		GIImageProcessing xmlImageProcessing = factory.createGIImageProcessing();
		xmlImageProcessing.setName(processing.getName());

		return xmlImageProcessing;
	}

}
