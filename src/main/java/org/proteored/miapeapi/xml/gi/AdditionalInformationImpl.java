package org.proteored.miapeapi.xml.gi;

import org.proteored.miapeapi.interfaces.gi.GIAdditionalInformation;
import org.proteored.miapeapi.xml.gi.autogenerated.ParamType;
import org.proteored.miapeapi.xml.gi.util.GIControlVocabularyXmlFactory;

public class AdditionalInformationImpl implements GIAdditionalInformation {
	private final ParamType additionalInfoXML;

	public AdditionalInformationImpl(ParamType addInfoXML) {
		this.additionalInfoXML = addInfoXML;
	}

	@Override
	public String getName() {
		return GIControlVocabularyXmlFactory.getName(additionalInfoXML);
	}

	@Override
	public String getValue() {
		return GIControlVocabularyXmlFactory.getValue(additionalInfoXML);
	}

}
