package org.proteored.miapeapi.xml.gi;

import org.proteored.miapeapi.interfaces.gi.AnalysisDesign;
import org.proteored.miapeapi.xml.gi.autogenerated.GIAnalysisDesign;

public class AnalysisDesignImpl implements AnalysisDesign {
	private final GIAnalysisDesign xmlAnalysisDesign;
	public AnalysisDesignImpl(GIAnalysisDesign xmlAnalysisDesign) {
		this.xmlAnalysisDesign = xmlAnalysisDesign;
	}

	@Override
	public String getExternalStandard() {
		return xmlAnalysisDesign.getExternalStandard();
	}

	@Override
	public String getGroups() {
		return xmlAnalysisDesign.getGroups();
	}

	@Override
	public String getName() {
		return xmlAnalysisDesign.getName();
	}

	@Override
	public String getReplicates() {
		return xmlAnalysisDesign.getReplicates();
	}

	@Override
	public String getStandard() {
		return xmlAnalysisDesign.getStandards();
	}

	@Override
	public String getType() {
		return xmlAnalysisDesign.getType();
	}

	@Override
	public String getWithinSampleStandard() {
		return xmlAnalysisDesign.getWithinSampleStandard();
	}

}
