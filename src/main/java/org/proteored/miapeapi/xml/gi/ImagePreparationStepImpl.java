package org.proteored.miapeapi.xml.gi;

import org.proteored.miapeapi.interfaces.gi.ImagePreparationStep;
import org.proteored.miapeapi.xml.gi.autogenerated.GIImagePreparationStep;

public class ImagePreparationStepImpl implements ImagePreparationStep {
	private final GIImagePreparationStep xmlPreparationStep;
	public ImagePreparationStepImpl(GIImagePreparationStep xmlPreparationStep) {
		this.xmlPreparationStep = xmlPreparationStep;
	}

	@Override
	public String getName() {
		return xmlPreparationStep.getName();
	}

	@Override
	public String getParameters() {
		return xmlPreparationStep.getParameters();

	}

	@Override
	public String getStepOrder() {
		return xmlPreparationStep.getStepOrder();

	}

	@Override
	public String getType() {
		return xmlPreparationStep.getType();

	}

}
