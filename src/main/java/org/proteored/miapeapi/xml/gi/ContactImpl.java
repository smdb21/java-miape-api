package org.proteored.miapeapi.xml.gi;

import org.proteored.miapeapi.interfaces.ge.GEContact;
import org.proteored.miapeapi.xml.gi.autogenerated.MIAPEContactType;
import org.proteored.miapeapi.xml.gi.util.GIControlVocabularyXmlFactory;

public class ContactImpl implements GEContact {

	private final MIAPEContactType contact;
	public ContactImpl(MIAPEContactType contact) {
		this.contact  = contact;
	}

	@Override
	public String getAddress() {
		return contact.getAddress();
	}

	@Override
	public String getCP() {
		return contact.getCP();
	}

	@Override
	public String getCountry() {
		return contact.getCountry();
	}

	@Override
	public String getDepartment() {
		return contact.getDepartment();
	}

	@Override
	public String getEmail() {
		return contact.getEmail();
	}

	@Override
	public String getFax() {
		return contact.getFax();
	}

	@Override
	public String getInstitution() {
		return contact.getInstitution();
	}

	@Override
	public String getLastName() {
		return contact.getLastName();
	}

	@Override
	public String getLocality() {
		return contact.getLocality();
	}

	@Override
	public String getName() {
		return contact.getName();
	}

	@Override
	public String getTelephone() {
		return contact.getTelephone();
	}

	@Override
	public String getPosition() {
		if (contact.getPosition() == null) return null;	
		return GIControlVocabularyXmlFactory.getName(contact.getPosition());
	}

}
